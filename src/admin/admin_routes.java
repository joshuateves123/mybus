    private void jPanel13MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel13MouseClicked
        String query = searchbar.getText();
        String searchQuery = "SELECT * FROM tbl_routes WHERE r_id LIKE '%" + query + "%' OR r_from LIKE '%" + query + "%' OR r_destination LIKE '%" + query + "%' OR r_fare LIKE '%"
        + query +"%' OR b_status LIKE '%" + query +"%'";

        try {
            dbConnector connect = new dbConnector();
            ResultSet rs = connect.getData(searchQuery);
            table.setModel(DbUtils.resultSetToTableModel(rs));
        } catch(SQLException ex) {
            System.out.println("Error searching users: " + ex.getMessage());
        }
    }//GEN-LAST:event_jPanel13MouseClicked

